        ListaCanciones.get(modindex-1).setCancion(jtf_CampoCancion.getText());
        ListaCanciones.get(modindex-1).setAlbum(jtf_CampoAlbum.getText());
        ListaCanciones.get(modindex-1).setArtista(jtf_CampoArtista.getText());
        ListaCanciones.get(modindex-1).setDuracion(Integer.parseInt(jFormattedCampoDuracion.getText()));
        ListaCanciones.get(modindex-1).setLanzamiento(Integer.parseInt(jFormattedCampoLanzamiento1.getText()));        
        

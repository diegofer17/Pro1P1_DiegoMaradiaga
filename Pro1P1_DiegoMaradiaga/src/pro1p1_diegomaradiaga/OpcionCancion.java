        String artista = (JOptionPane.showInputDialog(this, "Ingrese el nombre del artista: ",JOptionPane.QUESTION_MESSAGE));
        int duracion = Integer.parseInt(JOptionPane.showInputDialog(this, "Ingrese la duracion de la cancion (segundos): ",JOptionPane.QUESTION_MESSAGE));

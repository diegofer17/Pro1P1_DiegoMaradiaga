    private void InformacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InformacionActionPerformed
        int cont = 1;
        String linea = "";
        for (int i = 0; i < ListaCanciones.size(); i++) {
            linea += cont + ") " + ListaCanciones.get(i).getCancion() + " - " + ListaCanciones.get(i).getArtista() + "\n";
            cont++;
        }
        int indice =0;
            indice = Integer.parseInt(JOptionPane.showInputDialog(this, linea + ""
                + ""
                + "Ingrese el índice de la canción que desea ver la información: "));
        while(indice>ListaCanciones.size()||indice<=0){
            indice = Integer.parseInt(JOptionPane.showInputDialog(this, linea + ""
                + "¡Cuidado, índice fuera de rango!\n"
                + "Ingrese el índice de la canción que desea ver la información: "));
        }
         JOptionPane.showMessageDialog(this, "Cancion: " + ListaCanciones.get(indice - 1).getCancion() + "\n"
                + "Album: " + ListaCanciones.get(indice - 1).getAlbum() + "\n"
                + "Artista: " + ListaCanciones.get(indice - 1).getArtista() + "\n"
                + "Duracion: " + ListaCanciones.get(indice - 1).getDuracion() + " segundos"+"\n"
                + "Año de lanzamiento: " + ListaCanciones.get(indice - 1).getLanzamiento() + "\n");
       
    }//GEN-LAST:event_InformacionActionPerformed

